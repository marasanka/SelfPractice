public class SwitchStatement3 {
    public static void main(String[] args) {

    String browser = "safari " ;

    String selectedBrowser = "";


    switch (browser){
        case "chrome":
            selectedBrowser = "chrome";
            break;

        case "firefox":
            selectedBrowser = "firefox";
            break;
        case "opera":
            selectedBrowser = "opera";
            break;
        case "ie" :
            selectedBrowser= "ie";
            break;

        default:
            selectedBrowser = "No browser";


            System.out.println(selectedBrowser);












    }


        System.out.println(selectedBrowser);









    }
}
